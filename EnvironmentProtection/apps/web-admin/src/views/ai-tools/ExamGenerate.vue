  try {
    const res = await request.get(`/api/exam/papers/${paper.id}`, { params: { includeAnswer: false } })
    previewPaper.value = res.data
    showPreviewDialog.value = true
  } catch (e) {
    ElMessage.error('加载预览失败')
  }
