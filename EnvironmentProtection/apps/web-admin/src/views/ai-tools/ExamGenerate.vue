// 弹窗控制
const showAutoGenerateDialog = ref(false)
const showQuestionDialog = ref(false)
const showAddQuestionDialog = ref(false)
const showPreviewDialog = ref(false)
const showDispatchDialog = ref(false)

const previewPaper = ref(null)
const currentPaper = ref(null)

const generateForm = reactive({
  title: '',
  description: '',
  passScore: 60,
  timeLimit: 60,
  questionTypes: [
    { type: 'single_choice', count: 10, score: 5 },
    { type: 'multiple_choice', count: 5, score: 10 },
    { type: 'judge', count: 10, score: 3 }
  ],
  difficulty: { easy: 30, medium: 50, hard: 20 },
  categories: []
})

const editingQuestion = reactive({
  id: '',
  type: 'single_choice',
  difficulty: 'medium',
  content: '',
  options: [
    { key: 'A', value: '' },
    { key: 'B', value: '' },
    { key: 'C', value: '' },
    { key: 'D', value: '' }
  ],
  correctAnswer: '',
  score: 10,
  analysis: ''
})

const dispatchForm = reactive({
  dispatchType: 'organization',
  organizationIds: [],
  userIds: []
})

// 加载试卷列表
const loadPapers = async () => {
  paperLoading.value = true
  try {
    const res = await request.get('/api/exam/papers', { params: paperQuery })
    papers.value = res.data || []
    paperTotal.value = res.data?.total || papers.value.length
  } catch (e) {
    ElMessage.error('加载试卷失败')
  } finally {
    paperLoading.value = false
  }
}

// 加载考试记录
const loadRecords = async () => {
  recordLoading.value = true
  try {
    const res = await request.get('/api/exam/records', { params: recordQuery })
    records.value = res.data || []
    recordTotal.value = res.data?.total || records.value.length
  } catch (e) {
    ElMessage.error('加载记录失败')
  } finally {
    recordLoading.value = false
  }
