    ],
    correctAnswer: question.answer ? JSON.parse(question.answer)[0] : '',
    score: question.score,
    analysis: question.analysis || ''
  })
  showAddQuestionDialog.value = true
}

const saveQuestion = async () => {
  if (!editingQuestion.content) {
    return ElMessage.warning('请输入题目内容')
  }
  try {
    const data = { ...editingQuestion }
    data.options = editingQuestion.options
    data.answer = JSON.stringify([editingQuestion.correctAnswer])
    if (editingQuestion.id) {
      await request.put(`/api/exam/questions/${editingQuestion.id}`, data)
      ElMessage.success('更新成功')
    } else {
      await request.post('/api/exam/questions', data)
      ElMessage.success('添加成功')
    }
    showAddQuestionDialog.value = false
    loadQuestions()
  } catch (e) {
    ElMessage.error('保存失败')
  }
}

