  switch (cmd) {
    case 'publish':
      await handlePublish(paper)
      break
    case 'dispatch':
      showDispatchDialog.value = true
      break
    case 'statistics':
      await viewPaperStats({ paperId: paper.id, paperTitle: paper.title })
      activeTab.value = 'statistics'
      break
    case 'delete':
      await handleDelete(paper)
      break
  }
}

const handlePublish = async (paper) => {
  try {
    await request.post(`/api/exam/papers/${paper.id}/publish`)
    ElMessage.success('发布成功')
    loadPapers()
  } catch (e) {
    ElMessage.error('发布失败')
  }
